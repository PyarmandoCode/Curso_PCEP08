# todo notacion cientifica

# todo binario

num1 = "0b100"  # 4
num2 = "0b110"  # 6
sumar = int(num1, 2) + int(num2, 2)
print(bin(sumar))

# todo hexa

hnum1 = "0x10"
hnum2 = "0x10"
suma = int(hnum1, 16) + int(hnum2, 16)
print(hex(suma))

# todo octal

onum1 ="0o10"
onum2 ="0o10"
suma = int(onum1,8) + int(onum2,8)
print(oct(suma))

